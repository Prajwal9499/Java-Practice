package com.practice.day1;

public class DoWhilePractice {

	public static void main(String[] args) {
	     int a = 1;
	        do {
	            System.out.println("DO loop start: a = " + a); // Print the current value of a
	            a++; // Increment a
	        } while (a < 10); // Loop continues as long as a is less than 10

	        System.out.println("After loop, a = " + a); // Print the final value of a after the loop
	     
	    }
}
